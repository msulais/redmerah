export function rectWidth(rect: DOMRect): number {
	return rect.width
}

export function rectHeight(rect: DOMRect): number {
	return rect.height
}

export function rectTop(rect: DOMRect): number {
	return rect.top
}

export function rectRight(rect: DOMRect): number {
	return rect.right
}

export function rectBottom(rect: DOMRect): number {
	return rect.bottom
}

export function rectLeft(rect: DOMRect): number {
	return rect.left
}